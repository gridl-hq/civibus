def test_render_current_md_changes_when_source_changes(tmp_path: Path) -> None:
    """Cache-bug detection: idempotency alone could pass for the wrong
    reason if the implementation cached results across calls. Mutating a
    source file between calls must produce different output."""
    repo = _build_synthetic_repo(tmp_path)
    before = keel_current.render_current_md(repo_root=repo, today_utc=date(2026, 4, 26))
    # Add a new OQ heading to open_questions.md.
    oq_path = repo / "docs" / "keel" / "open_questions.md"
    oq_path.write_text(
        oq_path.read_text(encoding="utf-8") + "\n### 99. Brand new question\nbody.\n",
        encoding="utf-8",
    )
    after = keel_current.render_current_md(repo_root=repo, today_utc=date(2026, 4, 26))
    assert before != after
    assert "99. Brand new question" in after


