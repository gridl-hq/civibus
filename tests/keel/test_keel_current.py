def test_main_default_date_uses_utc_not_local_time(tmp_path: Path, monkeypatch) -> None:
    """Casual rollup must use the same UTC clock as `make keel-status` so
    the two never disagree on the same evidence's freshness when the
    operator's local TZ straddles UTC midnight.

    Defends against the regression we just fixed: `date.today()` uses
    local time and skews from `make keel-status`'s `datetime.now(UTC)`.

    Strategy: patch BOTH clocks to deterministic-but-DIFFERENT dates,
    then assert the UTC date wins. Patching only `datetime.now` would
    leave a TZ-dependent false-positive — on a UTC server, `date.today`
    happens to agree with UTC and a `date.today()`-based bad impl would
    also produce 2026-04-27, passing the test for the wrong reason.
    """
    repo = _build_synthetic_repo(tmp_path)
    utc_target_dt = datetime(2026, 4, 27, 0, 5, tzinfo=UTC)
    local_decoy_date = date(1999, 1, 1)  # known-wrong; far from any real run

    class _FrozenDateTime(datetime):
        @classmethod
        def now(cls, tz=None):  # type: ignore[override]
            return utc_target_dt

    class _FrozenDate(date):
        @classmethod
        def today(cls):  # type: ignore[override]
            return local_decoy_date

    monkeypatch.setattr(keel_current, "datetime", _FrozenDateTime)
    monkeypatch.setattr(keel_current, "date", _FrozenDate)
    rc = keel_current.main(["--repo-root", str(repo)])
    assert rc == 0
    out = (repo / "docs" / "keel" / "CURRENT.md").read_text(encoding="utf-8")
    assert "2026-04-27" in out, "casual rollup default date must come from UTC clock"
    assert "1999-01-01" not in out, "casual rollup must NOT pick up local-time date.today()"


def test_main_writer_idempotent_end_to_end(tmp_path: Path) -> None:
    repo = _build_synthetic_repo(tmp_path)
    keel_current.main(["--repo-root", str(repo), "--date", "2026-04-26"])
    a = (repo / "docs" / "keel" / "CURRENT.md").read_text(encoding="utf-8")
    keel_current.main(["--repo-root", str(repo), "--date", "2026-04-26"])
    b = (repo / "docs" / "keel" / "CURRENT.md").read_text(encoding="utf-8")
    assert a == b
