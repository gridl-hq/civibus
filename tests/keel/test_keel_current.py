_CHECKLIST_MD = """# Keel checklist

## Parking lot

**KNOWN_PARKING_LOT_CANARY_LINE_xyzz** — this is a unique substring used
by tests to confirm parking-lot content is NOT duplicated into CURRENT.md.
"""


def _build_synthetic_repo(tmp_path: Path) -> Path:
    repo = tmp_path / "repo"
    repo.mkdir()
    (repo / "layers.yaml").write_text(_LAYERS_YAML, encoding="utf-8")
    schemas = repo / "evidence_schemas"
    schemas.mkdir()
    (schemas / "L1.json").write_text(_L1_SCHEMA, encoding="utf-8")
    (schemas / "L12.json").write_text(_L1_SCHEMA, encoding="utf-8")
    (repo / "keel_reviews.yaml").write_text(_REVIEWS_YAML, encoding="utf-8")
    docs_keel = repo / "docs" / "keel"
    docs_keel.mkdir(parents=True)
    (docs_keel / "open_questions.md").write_text(_OPEN_QUESTIONS_MD, encoding="utf-8")
    (docs_keel / "checklist.md").write_text(_CHECKLIST_MD, encoding="utf-8")
    return repo


def test_render_current_md_starts_with_sentinel(tmp_path: Path) -> None:
    repo = _build_synthetic_repo(tmp_path)
    out = keel_current.render_current_md(repo_root=repo, today_utc=date(2026, 4, 26))
    first_line = out.splitlines()[0]
    assert first_line == "<!-- GENERATED by make keel-current; hand edits will be overwritten. -->"


def test_render_current_md_contains_today_date(tmp_path: Path) -> None:
    repo = _build_synthetic_repo(tmp_path)
    out = keel_current.render_current_md(repo_root=repo, today_utc=date(2026, 4, 26))
    head = "\n".join(out.splitlines()[:3])
    assert "2026-04-26" in head


def test_render_current_md_layer_status_section_non_empty(tmp_path: Path) -> None:
    repo = _build_synthetic_repo(tmp_path)
    out = keel_current.render_current_md(repo_root=repo, today_utc=date(2026, 4, 26))
    assert "## Layer status" in out
    # Each layer in layers.yaml appears as a `### {layer_id}` heading.
    assert re.search(r"^### L1\b", out, re.MULTILINE)
    assert re.search(r"^### L12\b", out, re.MULTILINE)
    # Non-empty interpretation lines under each layer block.
    layer_status_idx = out.index("## Layer status")
    # Use `\n## ` to anchor at start-of-line; otherwise "### L1" matches "## ".
    next_section = out.index("\n## ", layer_status_idx + 5)
    block = out[layer_status_idx:next_section]
    assert "all 1 scopes errored" in block or "no evidence emitted yet" in block
    assert "per-session summary; no snapshot evidence" in block


